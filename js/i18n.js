const I18n = {
    currentLanguage: 'en',

    init() {
        const savedLang = localStorage.getItem('preferred_language');
        if (savedLang && translations[savedLang]) {
            this.setLanguage(savedLang);
        } else {
            // Check browser language or default to en
            const browserLang = navigator.language.split('-')[0];
            if (translations[browserLang]) {
                this.setLanguage(browserLang);
            } else {
                this.setLanguage('en');
            }
        }
    },

    setLanguage(lang) {
        if (!translations[lang]) return;

        this.currentLanguage = lang;
        localStorage.setItem('preferred_language', lang);

        // Update document attributes
        document.documentElement.lang = lang;

        if (lang === 'ar') {
            document.documentElement.dir = 'rtl';
            document.body.classList.add('rtl');
        } else {
            document.documentElement.dir = 'ltr';
            document.body.classList.remove('rtl');
        }

        this.updatePageContent();
        this.updatePlaceholder();

        // Update Title
        if (translations[lang]['page_title']) {
            document.title = translations[lang]['page_title'];
        }
    },

    updatePageContent() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = translations[this.currentLanguage][key];
            if (translation) {
                // If element has an icon (SVG), preserve it
                const svg = element.querySelector('svg');
                if (svg) {
                    // Find the text node to replace or append
                    // Simple approach: append translation to SVG or replace text
                    element.innerHTML = svg.outerHTML + ' ' + translation;
                } else {
                    element.innerHTML = translation;
                }
            }
        });
    },

    updatePlaceholder() {
        const inputs = document.querySelectorAll('[data-i18n-placeholder]');
        inputs.forEach(input => {
            const key = input.getAttribute('data-i18n-placeholder');
            const translation = this.translate(key);
            if (translation) {
                input.placeholder = translation;
            }
        });
    },

    translate(key) {
        if (translations[this.currentLanguage] && translations[this.currentLanguage][key]) {
            return translations[this.currentLanguage][key];
        }
        // Fallback to English
        if (translations['en'] && translations['en'][key]) {
            return translations['en'][key];
        }
        return key;
    }
};

// Expose to window immediately
window.I18n = I18n;

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    I18n.init();
});
